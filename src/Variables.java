public class Variables {
    public static void main(String[] args){
        // String is used to only names
        String name = "Love";
        System.out.println(name);

        String cityname = "Noida";
        System.out.println(cityname);

        String villagename = "Pinahat";
        System.out.println(villagename);

        //int is used to numeric values
        int price = 22;
        System.out.println(price);

        int bestprice = 65;
        System.out.println(bestprice);

        int homeno = 5645;
        System.out.println(homeno);

        // double is used to decimal no
        // double is used for  point no
        double marks = 87.44;
        System.out.println(marks);

        double percentage = 565.67;
        System.out.println(percentage);




    }
}
